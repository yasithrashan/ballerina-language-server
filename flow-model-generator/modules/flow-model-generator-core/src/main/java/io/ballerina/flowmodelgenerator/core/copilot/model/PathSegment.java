package io.ballerina.flowmodelgenerator.core.copilot.model;

/**
 * Represents a path parameter in a resource function.
 *
 * @since 1.7.0
 */
public final class PathSegment implements PathElement {
    private String name;
    private String type;

    public PathSegment() {
    }

    public PathSegment(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
